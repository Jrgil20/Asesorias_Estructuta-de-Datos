#include <iostream>
using namespace std;

int main() {
    // Tipo de dato entero
    int numero = 10;
    cout << "Número entero: " << numero << endl;

    // Tipo de dato decimal
    float decimal = 3.14;
    cout << "Número decimal: " << decimal << endl;

    // Tipo de dato caracter
    char letra = 'A';
    cout << "Caracter: " << letra << endl;

    // Tipo de dato booleano
    bool verdadero = true;
    bool falso = false;
    cout << "Booleano verdadero: " << verdadero << endl;
    cout << "Booleano falso: " << falso << endl;

    // Tipo de dato cadena de caracteres
    string cadena = "Hola, mundo!";
    cout << "Cadena de caracteres: " << cadena << endl;

    return 0;
}